/**
    - Collection adalah hal umum yang biasa dimiliki oleh bahasa pemrograman, seperti Java, Python, Ruby, dll.
    - Sebuah collection biasanya berisikan sejumlah data, bahkan dapat kosong.
    - Kotlin standard library menyediakan data Collection yang sudah dapat langsung kita gunakan, tanpa tambahan library.
    - Secara default collection di Kotlin adalah Immutable.

    Tipe Collection
    secara garis besar, ada 2 jenis collection pada Kotlin.
        1. Immutable, collection yang hanya mendukung operasi read only (data-nya tidak dapat berubah).
        2. Mutable, collection turunan Immutable, namun mendukung operasi write juga seperti add, edit, dan remove
           sehingga data-nya dapat diubah.

 */